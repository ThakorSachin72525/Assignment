// Ternary Conditions
// Q-1. Write a program that takes in a number and outputs whether it is positive, negative, or zero.
// Ans 
let num = 5;
num == 0
    ? console.log("This is zero")
    : num > 0
    ? console.log("This is positive")
    : console.log("This is negative");

// Q-2. Create a program that takes in two numbers and prints the larger one.
// Ans 
let numA = 3;
let numB = 4;
numA > numB
    ? console.log(numA)
    : console.log(numB);
